import mongoose from "mongoose";
const todoSchema = new mongoose.Schema({
    id: {
        type: String,
        required: true,
        unique: true,
    },
    title: {
        type: String,
        required: true,
    },
    description: {
        type: String,
        required: true,
    },
    completed: {
        type: Boolean,
        required: true,
    },
});

export const todoModal = mongoose.model("todos", todoSchema);

export default todoSchema;